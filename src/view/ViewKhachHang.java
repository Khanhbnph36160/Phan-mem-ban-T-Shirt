    private KhachHang getDataKhachHangByForm() {
        String id = this.jblId.getText();
        String hoTen = this.txthoten.getText();
        boolean isMale = this.rdoNam.isSelected();
        String soDienThoai = this.txtsdt.getText();
        String diaChi = this.txtDiaChi.getText();

        KhachHang kh = new KhachHang();
        kh.setId("-".equals(id) || "".equals(id) ? null : Integer.valueOf(id));
        kh.setHoTen(hoTen);
        kh.setGioiTinh(isMale ? 1 : 0);
        kh.setSdt(soDienThoai);
        kh.setDiaChi(diaChi);

        return kh;
    }

    private void loadFormProperties(String name) {
        DefaultTableModel dtm = (DefaultTableModel) this.tblKhachHang.getModel();
        dtm.setRowCount(0);
        int stt = 1;

        for (KhachHang kh : khsv.findAll(name)) {
            Object[] rowData = {
                stt,
                kh.getId(),
                kh.getHoTen(),
                kh.getGioiTinh(),
                kh.getSdt(),
                kh.getDiaChi(),
                kh.getNgayTao(),};
            dtm.addRow(rowData);
            stt++;
        }

    }

