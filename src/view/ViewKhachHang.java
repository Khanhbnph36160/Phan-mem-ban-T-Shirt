    private void tblKhachHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKhachHangMouseClicked
        int row = this.tblKhachHang.getSelectedRow();

        if (row <= -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng bạn muốn chọn");
            return;
        }

        String id = this.tblKhachHang.getValueAt(row, 1).toString();
        String HoTen = this.tblKhachHang.getValueAt(row, 2).toString();
        String gioiTinh = this.tblKhachHang.getValueAt(row, 3).toString();
        String sdt = this.tblKhachHang.getValueAt(row, 4).toString();
        String diaChi = this.tblKhachHang.getValueAt(row, 5).toString();
        String ngayTao = this.tblKhachHang.getValueAt(row, 6).toString();

        this.idKhachHang = id;
        this.jblId.setText(id);
        this.txthoten.setText(HoTen);
        this.rdoNam.setSelected("Nam".equals(gioiTinh)); // Sửa lại để chọn đúng RadioButton
        this.rdoNu.setSelected("Nữ".equals(gioiTinh));
        this.txtsdt.setText(sdt);
        this.txtDiaChi.setText(diaChi);
        this.jlbNgayTao.setText(ngayTao);
    }//GEN-LAST:event_tblKhachHangMouseClicked

